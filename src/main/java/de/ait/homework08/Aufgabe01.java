package de.ait.homework08;

public class Aufgabe01 {
    public static void main(String[] args) {
        /*
        Write a program that will count from 10 to 1
         in reverse order using a do-while loop. 10,9,8,7...1
         */
        int x = 11;
        do {
            x--;
            System.out.println(" " + x);
        }
        while (x != 1);

    }
}
